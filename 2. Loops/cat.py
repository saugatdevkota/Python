#using while loop to print "Meow" 3 times.
i = 0
while i < 3:
    print("Meow")
    i += 1

print("_____________________________________________")

#Simply goes in list and executes as long as there are items in the list.
for i in [0, 1, 2]:
    print("Meow")
    
